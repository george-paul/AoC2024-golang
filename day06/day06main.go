var runeToUint = map[rune]uint8{
	rune('.'): 0,
	rune('#'): 1,
	rune('^'): 2,
}

func Day06Main() {
	var grid Grid
	begR := 0
	begC := 0
	for r, line := range strings.Split(puzzleInput, "\n") {
		for c, char := range line {
			if runeToUint[char] == 2 {
				begR = r
				begC = c
			}
			grid[r][c] = runeToUint[char]
		}
	}
	util.ResultString(6, d06p01(grid, begR, begC))
}

